SELECT
    IMP_DESTINO AS MONTO_IMP_DESTINO,
    COUNT(DISTINCT NUM_CUENTA) AS NUM_CUENTAS_UNICAS
FROM PROSA_510
WHERE 
    FECHA_CONSUMO >= '2023-01-01 00:00:00.000'
    AND FECHA_CONSUMO <= '2023-10-05 00:00:00.000'
    AND TIPO_TRANSACCION = '01' 
    --AND IMP_DESTINO >= '1500'
    AND (RFC_COMERCIO = 'ODO 910311U70'
         OR RFC_COMERCIO = 'OPP 010927SA5'
         OR RFC_COMERCIO = 'OAP 1411219D4')
GROUP BY IMP_DESTINO
ORDER BY IMP_DESTINO;

