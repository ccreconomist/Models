SELECT
    NUM_CUENTA,
    FECHA_CONSUMO,
    TIPO_TRANSACCION,
	RFC_COMERCIO,
	NUM_AUT,
    IMP_DESTINO,
    LEFT(NUM_CUENTA, 6) AS NO_DE_CUENTA,
    '000' + SUBSTRING(NUM_CUENTA, 1, LEN(NUM_CUENTA) - 3) + '000' AS CUENTA2
FROM PROSA_510
WHERE
    FECHA_CONSUMO >= '2023-10-20 00:00:00.000' --PERIODO INICIO 
    AND FECHA_CONSUMO <= '2023-10-25 00:00:00.000' --PERIODO FINAL
    AND TIPO_TRANSACCION = '01'
	AND RFC_COMERCIO = 'GCD 170101GS6'
    --AND IMP_DESTINO >= 500 ---MONTOS MODIFICADOS PARA CONDICION DE CAMPAÑA
	----AND IMP_DESTINO <= 5000 ----MONTOS MODIFICADOS PARA CONDICION DE CAMPAÑAS LIMITE
	AND LEFT(NUM_CUENTA, 6) IN ('506370', '481282'); --BINES DE GCC