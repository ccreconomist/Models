SELECT 
    NUM_CUENTA, 
    FECHA_CONSUMO, 
    TIPO_TRANSACCION, 
    IMP_DESTINO, 
    MCC_GIRO_COMERCIO, 
    LEFT(NUM_CUENTA, 6) AS NO_DE_CUENTA_NUMERICO,
    CONCAT('000', SUBSTRING(NUM_CUENTA, 1, LEN(NUM_CUENTA) - 3), '000') AS NO_DE_CUENTA_TEXTO
FROM PROSA_510
WHERE 
    FECHA_CONSUMO >= '2023-10-01 00:00:00.000' AND 
    FECHA_CONSUMO <= '2023-10-29 00:00:00.000' AND
    TIPO_TRANSACCION = '01' AND
    MCC_GIRO_COMERCIO = '07832' AND
    LEFT(NUM_CUENTA, 6) IN ('481284', '481281', '403750', '464811', '481282', '446351', '481283', '469849');

	
SELECT
    FECHA_CONSUMO,
    COUNT(DISTINCT NUM_CUENTA) AS NUM_CUENTAS_UNICAS,
    COUNT(IMP_DESTINO) AS TXN,
    SUM(IMP_DESTINO) AS MONTO_IMP_DESTINO_POR_DIA
FROM PROSA_510
WHERE 
    FECHA_CONSUMO >= '2022-01-01 00:00:00.000' AND 
    FECHA_CONSUMO <= '2023-10-12 00:00:00.000' AND
    TIPO_TRANSACCION = '01' AND
    (RFC_COMERCIO = 'ODO 910311U70' OR RFC_COMERCIO = 'OPP 010927SA5' OR RFC_COMERCIO = 'OAP 1411219D4') AND
    LEFT(NUM_CUENTA, 6) IN ('481284', '481281', '403750', '481282', '446351', '481283')
GROUP BY FECHA_CONSUMO
ORDER BY FECHA_CONSUMO;