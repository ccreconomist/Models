SELECT 
    FECHA_CONSUMO,
    LEFT(NUM_CUENTA, 6) AS NO_DE_CUENTA,
    COUNT(DISTINCT NUM_CUENTA) AS NUM_CUENTAS_UNICAS,
    COUNT(IMP_DESTINO) AS IMP_DESTINO_POR_DIA,
    SUM(IMP_DESTINO) AS MONTO_IMP_DESTINO_POR_DIA
FROM PROSA_510
WHERE 
    FECHA_CONSUMO >= '2023-01-01 00:00:00.000' AND 
    FECHA_CONSUMO <= '2023-09-1 00:00:00.000' AND 
    MCC_GIRO_COMERCIO = '07832' AND 
    TIPO_TRANSACCION = '01' AND 
    LEFT(NUM_CUENTA, 6) IN ('506372', '481281') ----AND
    ---RFC_COMERCIO = 'SSH 9608016NA'
GROUP BY FECHA_CONSUMO, LEFT(NUM_CUENTA, 6)
