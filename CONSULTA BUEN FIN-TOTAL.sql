USE BANKCARD

SELECT
    COUNT(distinct NUM_CUENTA) AS Cuenta,
    COUNT(NUM_AUT) AS TXN,
    SUM(IMP_DESTINO) AS Resultado
FROM PROSA_510
WHERE 
    FECHA_CONSUMO >= '2022-11-18 00:00:00.000' AND 
    FECHA_CONSUMO <= '2022-11-21 00:00:00.000' AND
	IMP_DESTINO >= '0' AND
    TIPO_TRANSACCION = '01' AND
    LEFT(NUM_CUENTA, 6) IN ('481284', '481281', '403750', '464811', '481282', '446351', '481283')

GROUP BY Cuenta
--HAVING SUM(CASE WHEN IMP_DESTINO >= 3000 THEN IMP_DESTINO ELSE 0 END) >= 3000;