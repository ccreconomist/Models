SELECT
    TARJETA,
    ACTIVACION,
    ULTC,
	CUENTA,
    LEFT(REPLACE(SUBSTRING(CUENTA, 4, LEN(CUENTA)), '0', ''), 6) AS NO_DE_CUENTA
FROM
    IT_CARTERA_PROMODA
WHERE
    ACTIVACION >= '2023-01-01 00:00:00.000'
    AND ACTIVACION <= '2023-08-25 00:00:00.000'
	AND LEFT(NO_DE_CUENTA, 6) IN ('403750', '506469');
