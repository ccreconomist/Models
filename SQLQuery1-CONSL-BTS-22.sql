SELECT NUM_CUENTA, FECHA_CONSUMO, TIPO_TRANSACCION, IMP_DESTINO, LEFT(NUM_CUENTA, 6) AS NO_DE_CUENTA
FROM PROSA_510
WHERE FECHA_CONSUMO >= '2022-06-01 00:00:00.000' AND 
FECHA_CONSUMO <= '2022-08-21 00:00:00.000' AND 
TIPO_TRANSACCION = '01' 
AND 
LEFT(NUM_CUENTA, 6) IN ('481284', '481281', '403750', '464811', '481282', '446351', '469849', '481283')
AND
IMP_DESTINO > 2000;
