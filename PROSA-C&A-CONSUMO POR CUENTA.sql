SELECT 
  NUM_CUENTA, 
  LEFT(NUM_CUENTA, 6) AS NO_DE_CUENTA,
  '000' + SUBSTRING(NUM_CUENTA, 1, LEN(NUM_CUENTA) - 3) + '000' AS CUENTA2,
  SUM(IMP_DESTINO) AS SUMA_IMP_DESTINO
FROM 
  PROSA_510
WHERE 
  FECHA_CONSUMO >= '2023-01-01 00:00:00.000' AND 
  FECHA_CONSUMO <= '2023-11-13 00:00:00.000' AND 
  TIPO_TRANSACCION = '01' AND 
  LEFT(NUM_CUENTA, 6) IN ('286900', '506329', '446351','286900','286900')
--AND IMP_DESTINO > 2000
GROUP BY 
  NUM_CUENTA, 
  LEFT(NUM_CUENTA, 6),
  '000' + SUBSTRING(NUM_CUENTA, 1, LEN(NUM_CUENTA) - 3) + '000';

