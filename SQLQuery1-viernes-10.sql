SELECT
    SUM(CASE 
        WHEN IMP_DESTINO <= 1000 THEN 1 
        ELSE 0 
    END) AS Compras_Menor_1000,
    SUM(CASE 
        WHEN IMP_DESTINO > 1000 AND IMP_DESTINO <= 1500 THEN 1 
        ELSE 0 
    END) AS Compras_1000_a_1500,
    SUM(CASE 
        WHEN IMP_DESTINO > 1500 AND IMP_DESTINO <= 2000 THEN 1 
        ELSE 0 
    END) AS Compras_1500_a_2000,
    SUM(CASE 
        WHEN IMP_DESTINO > 2000 AND IMP_DESTINO <= 3000 THEN 1 
        ELSE 0 
    END) AS Compras_2000_a_3000,
    SUM(CASE 
        WHEN IMP_DESTINO > 3000 AND IMP_DESTINO <= 4000 THEN 1 
        ELSE 0 
    END) AS Compras_3000_a_4000,
    SUM(CASE 
        WHEN IMP_DESTINO > 4000 THEN 1 
        ELSE 0 
    END) AS Compras_Mayor_4000,
	---CUENTAS
    COUNT(DISTINCT CASE 
        WHEN IMP_DESTINO < 1000 THEN NUM_CUENTA 
        ELSE NULL 
    END) AS Cuentas_Menor_1000,
    COUNT(DISTINCT CASE 
        WHEN IMP_DESTINO >= 1000 AND IMP_DESTINO < 1500 THEN NUM_CUENTA 
        ELSE NULL 
    END) AS Cuentas_1000_a_1500,
    COUNT(DISTINCT CASE 
        WHEN IMP_DESTINO >= 1500 AND IMP_DESTINO < 2000 THEN NUM_CUENTA 
        ELSE NULL 
    END) AS Cuentas_1500_a_2000,
    COUNT(DISTINCT CASE 
        WHEN IMP_DESTINO >= 2000 AND IMP_DESTINO < 3000 THEN NUM_CUENTA 
        ELSE NULL 
    END) AS Cuentas_2000_a_3000,
    COUNT(DISTINCT CASE 
        WHEN IMP_DESTINO >= 3000 AND IMP_DESTINO < 4000 THEN NUM_CUENTA 
        ELSE NULL 
    END) AS Cuentas_3000_a_4000,
    COUNT(DISTINCT CASE 
        WHEN IMP_DESTINO >= 4000 THEN NUM_CUENTA 
        ELSE NULL 
    END) AS Cuentas_Mayor_4000,
	---SUMA IMP
	    SUM( CASE 
        WHEN IMP_DESTINO < 1000 THEN IMP_DESTINO 
        ELSE NULL 
    END) AS Cuentas_Menor_1000,
    SUM( CASE 
        WHEN IMP_DESTINO >= 1000 AND IMP_DESTINO < 1500 THEN IMP_DESTINO 
        ELSE NULL 
    END) AS Cuentas_1000_a_1500,
    SUM( CASE 
        WHEN IMP_DESTINO >= 1500 AND IMP_DESTINO < 2000 THEN IMP_DESTINO 
        ELSE NULL 
    END) AS Cuentas_1500_a_2000,
    SUM(DISTINCT CASE 
        WHEN IMP_DESTINO >= 2000 AND IMP_DESTINO < 3000 THEN IMP_DESTINO 
        ELSE NULL 
    END) AS Cuentas_2000_a_3000,
    SUM( CASE 
        WHEN IMP_DESTINO >= 3000 AND IMP_DESTINO < 4000 THEN IMP_DESTINO 
        ELSE NULL 
    END) AS Cuentas_3000_a_4000,
    SUM( CASE 
        WHEN IMP_DESTINO >= 4000 THEN IMP_DESTINO 
        ELSE NULL 
    END) AS Cuentas_Mayor_4000

FROM PROSA_510
WHERE 
    FECHA_CONSUMO >= '2022-11-18 00:00:00.000' AND 
    FECHA_CONSUMO <= '2022-11-21 00:00:00.000' AND
    TIPO_TRANSACCION = '01' AND
    LEFT(NUM_CUENTA, 6) IN ('481284', '481281', '403750', '464811', '481282', '446351', '481283');