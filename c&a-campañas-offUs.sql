SELECT
    p1.FECHA_CONSUMO,
    p1.NUM_CUENTA,
    p2.TOTAL_IMP_DESTINO
FROM (
    SELECT
        FECHA_CONSUMO,
        NUM_CUENTA
    FROM PROSA_510
    WHERE 
        FECHA_CONSUMO >= '2023-10-16 00:00:00.000' AND 
        FECHA_CONSUMO <= '2023-10-27 00:00:00.000' AND
        TIPO_TRANSACCION = '01' AND
        LEFT(NUM_CUENTA, 6) IN ('286900', '506329', '446351')
    GROUP BY FECHA_CONSUMO, NUM_CUENTA
) p1
JOIN (
    SELECT
        FECHA_CONSUMO,
        NUM_CUENTA,
        SUM(IMP_DESTINO) AS TOTAL_IMP_DESTINO
    FROM PROSA_510
    WHERE 
        FECHA_CONSUMO >= '2023-10-16 00:00:00.000' AND 
        FECHA_CONSUMO <= '2023-10-27 00:00:00.000' AND
        TIPO_TRANSACCION = '01' AND
        LEFT(NUM_CUENTA, 6) IN ('286900', '506329', '446351')
    GROUP BY FECHA_CONSUMO, NUM_CUENTA
) p2 ON p1.FECHA_CONSUMO = p2.FECHA_CONSUMO AND p1.NUM_CUENTA = p2.NUM_CUENTA;

