SELECT
    NUM_CUENTA,
    FECHA_CONSUMO,
    TIPO_TRANSACCION,
    IMP_DESTINO,
    LEFT(NUM_CUENTA, 6) AS NO_DE_CUENTA,
    '000' + SUBSTRING(NUM_CUENTA, 1, LEN(NUM_CUENTA) - 3) + '000' AS CUENTA2
FROM PROSA_510
WHERE
    FECHA_CONSUMO >= '2023-08-25 00:00:00.000'
    AND FECHA_CONSUMO <= '2023-08-27 00:00:00.000'
    AND TIPO_TRANSACCION = '01'
    AND IMP_DESTINO >= 500
	AND IMP_DESTINO <= 5000
	AND LEFT(NUM_CUENTA, 6) IN ('506370', '481282');